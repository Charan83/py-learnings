#   unittest
#   - https://docs.python.org/3/library/unittest.html
#   - build-in module comes with python
#   - you can write unit tests encapsulated as classes that inherit from unittest.TestCase
#   - inheritance gives us access to many assertion helpers
#   - run the tests by calling unittest.main()
#   - python3 -m unittest activities_tests.py -v  --> more verbose

#   - assertEqual(is_True, False) : Will check for exact value to be False
#   - assertFalse(is_True, False) : Will check if the value is any falsy value
